<!-- JavaScript for 3D Stack Carousel -->
<script>
document.addEventListener('DOMContentLoaded', function() {
    const carousel = document.getElementById('stack-carousel');
    const stackItems = document.querySelectorAll('.stack-item');
    const counterDisplay = document.getElementById('counter-display');
    const navButtons = document.querySelectorAll('.nav-btn');
    
    let currentIndex = 0;
    const totalItems = stackItems.length;
    
    // Update counter display
    function updateCounter() {
        if (counterDisplay) {
            counterDisplay.textContent = `${currentIndex + 1}/${totalItems}`;
        }
    }
    
    // Update carousel position
    function updateCarousel() {
        stackItems.forEach((item, index) => {
            // Remove all classes
            item.classList.remove('active', 'prev', 'next', 'hidden');
            
            if (index === currentIndex) {
                item.classList.add('active');
            } else if (index === (currentIndex - 1 + totalItems) % totalItems) {
                item.classList.add('prev');
            } else if (index === (currentIndex + 1) % totalItems) {
                item.classList.add('next');
            } else {
                item.classList.add('hidden');
            }
        });
        
        updateCounter();
    }
    
    // Navigate to specific index
    function goToSlide(newIndex) {
        currentIndex = ((newIndex + totalItems) % totalItems);
        updateCarousel();
    }
    
    // Navigation functions
    function nextSlide() {
        goToSlide(currentIndex + 1);
    }
    
    function prevSlide() {
        goToSlide(currentIndex - 1);
    }
    
    // Navigation button event listeners
    navButtons.forEach(btn => {
        btn.addEventListener('click', e => {
            e.preventDefault();
            const increment = +btn.dataset.inc;
            if (increment === 1) {
                nextSlide();
            } else if (increment === -1) {
                prevSlide();
            }
        });
    });
    
    // Keyboard navigation
    document.addEventListener('keydown', e => {
        if (e.key === 'ArrowLeft') {
            prevSlide();
        } else if (e.key === 'ArrowRight') {
            nextSlide();
        }
    });
    
    // Touch/swipe support
    let startX = 0;
    let startY = 0;
    let endX = 0;
    let endY = 0;
    
    carousel.addEventListener('touchstart', e => {
        startX = e.touches[0].clientX;
        startY = e.touches[0].clientY;
    });
    
    carousel.addEventListener('touchend', e => {
        endX = e.changedTouches[0].clientX;
        endY = e.changedTouches[0].clientY;
        handleSwipe();
    });
    
    function handleSwipe() {
        const deltaX = endX - startX;
        const deltaY = endY - startY;
        const minSwipeDistance = 50;
        
        if (Math.abs(deltaX) > Math.abs(deltaY) && Math.abs(deltaX) > minSwipeDistance) {
            if (deltaX > 0) {
                prevSlide(); // swipe right = previous
            } else {
                nextSlide(); // swipe left = next
            }
        }
    }
    
    // Enhanced button interactions
    navButtons.forEach(btn => {
        btn.addEventListener('mouseenter', () => {
            btn.style.setProperty('--hov', '1');
        });
        
        btn.addEventListener('mouseleave', () => {
            btn.style.setProperty('--hov', '0');
        });
        
        // Add ripple effect on click
        btn.addEventListener('click', function(e) {
            const ripple = document.createElement('div');
            const rect = this.getBoundingClientRect();
            const size = Math.max(rect.width, rect.height);
            const x = e.clientX - rect.left - size / 2;
            const y = e.clientY - rect.top - size / 2;
            
            ripple.style.cssText = `
                position: absolute;
                width: ${size}px;
                height: ${size}px;
                left: ${x}px;
                top: ${y}px;
                background: rgba(255, 255, 255, 0.3);
                border-radius: 50%;
                transform: scale(0);
                animation: ripple 0.6s ease-out;
                pointer-events: none;
            `;
            
            this.appendChild(ripple);
            
            setTimeout(() => {
                ripple.remove();
            }, 600);
        });
    });
    
    // Add ripple animation
    const style = document.createElement('style');
    style.textContent = `
        @keyframes ripple {
            to {
                transform: scale(2);
                opacity: 0;
            }
        }
        
        .nav-btn {
            position: relative;
            overflow: hidden;
        }
    `;
    document.head.appendChild(style);
    
    // Initialize carousel
    updateCarousel();
    
    // Debug: Log current state
    console.log(`Carousel initialized with ${totalItems} items, manual navigation only`);
});
</script>
